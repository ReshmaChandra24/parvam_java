class TypeCasting {
    	public static void main(String args[]){
		int a=10,b=20;
		double c=a+b;
		System.out.println("First Number "+a);
		System.out.println("Second Number "+b);
		System.out.println("sum"+c);
		double d=a/b;
		System.out.println("The quotient "+d);
		int e=(int)d;
		System.out.println("the casted variable="+e);
	}
}